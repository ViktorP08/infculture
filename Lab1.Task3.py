# Расчет количества книг на дискете
# Описание
# У вас имеется информационный объем дискеты, равный 1,44 Мб, а также параметры книги, 
# такие как количество страниц, число строк на странице и количество символов в строке.
# Ваша задача — рассчитать, сколько одинаковых книг можно поместить на дискету, и вывести результат на экран.
# TODO Найдите количество книг, которое можно разместить на дискете
# Данные
disk_capacity_mb = 1.44  # Объем дискеты в Мб
pages_in_book = 100       # Количество страниц в книге
lines_per_page = 50       # Количество строк на странице
chars_per_line = 25       # Количество символов в строке
bytes_per_char = 4        # Количество байт для хранения одного символа

# Переводим объем дискеты в байты
disk_capacity_bytes = disk_capacity_mb * 1024 * 1024  # 1 Мб = 1024 Кб = 1024 * 1024 байт

# Рассчитываем объем одной книги
book_size_bytes = pages_in_book * lines_per_page * chars_per_line * bytes_per_char

# Рассчитываем количество книг, которые можно разместить на дискете
number_of_books = int(disk_capacity_bytes // book_size_bytes)


print("Количество книг, помещающихся на дискету:", number_of_books)
